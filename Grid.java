package needForSpeed;

public interface Grid {
	
	/**
	 * Get the center of the Arena
	 * @return The point object of the center
	 */
	public Point getCenter();
	
	
	/**
	 * return the half width of the arena
	 * @return
	 */
	public double getHalfWidth();
	
	
	/**
	 * get the half height of the arena
	 * @return
	 */
	public double getHalfHeight();
	
	
	/**
	 * get the number of rows of the arena
	 * @return
	 */
	public int getRows();
	
	
	/**
	 * get the number of cols of the arena
	 * @return
	 */
	public int getCols();

}
